  renderNavbar(activePage) {
    const items = [
      { id: 'dashboard', label: 'Anasayfa', icon: 'home' },
      { id: 'timer', label: 'Zamanlayıcı', icon: 'clock' },
      { id: 'daily-log', label: 'Kayıt', icon: 'book' },
      { id: 'analysis', label: 'Analiz', icon: 'chart' },
      { id: 'settings', label: 'Ayarlar', icon: 'settings' }
    ];

    return `
      <nav class="bottom-nav">
        ${items.map(item => `
          <button class="nav-item ${activePage === item.id ? 'active' : ''}"
                  onclick="navigateTo('${item.id}')">
            ${UI.createIcon(item.icon)}
            <span>${item.label}</span>
          </button>
        `).join('')}
      </nav>
    `;
  },

  renderEmptyState(message, icon = 'book') {
    return `
      <div class="empty-state">
        ${UI.createIcon(icon, 64)}
        <p>${message}</p>
      </div>
    `;
  },

  renderProgressBar(percentage, color = 'var(--primary)') {
    return `
      <div class="progress-bar">
        <div class="progress-fill" style="width: ${Math.min(100, percentage)}%; background: ${color}"></div>
      </div>
    `;
  },

  renderSelectOptions(items, valueKey, labelKey, selected = null) {
    return items.map(item => `
      <option value="${item[valueKey]}" ${item[valueKey] == selected ? 'selected' : ''}>
        ${item[labelKey]}
      </option>
    `).join('');
  }
